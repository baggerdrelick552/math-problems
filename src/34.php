<?php
function solve_math_problems() {
    // Example: Find the sum of two numbers
    $a = 5;
    $b = 3;
    $sum = $a + $b;
    echo "The sum of $a and $b is $sum.";

    // Example: Check if a number is even or odd
    $number = 10;
    if ($number % 2 == 0) {
        echo "$number is an even number.";
    } else {
        echo "$number is an odd number.";
    }
}
solve_math_problems();
