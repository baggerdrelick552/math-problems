<?php
// Example PHP code related to math-problems

// Problem 1: Find the area of a rectangle with length 5 and width 3.
function rectangle_area($length, $width) {
    return $length * $width;
}

// Example output: The area is 15.

// Problem 2: Calculate the perimeter of a circle with radius 4.
function circle_perimeter($radius) {
    return 2 * M_PI * $radius;
}

// Example output: The perimeter is approximately 25.13.
