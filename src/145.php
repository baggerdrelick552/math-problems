<?php
// Sample PHP code for mathematics problems related to grades

$grade = 85; // Expected grade score

if ($grade >= 90) {
    echo "Your grade is A";
} else if ($grade >= 85) {
    echo "Your grade is B";
} else if ($grade >= 75) {
    echo "Your grade is C";
} else if ($grade >= 65) {
    echo "Your grade is D";
} else {
    echo "Your grade is F";
}
?>
