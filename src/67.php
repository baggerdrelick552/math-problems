<?php
// Example PHP code to demonstrate basic operations and variables

// Define two integers
$a = 10;
$b = 5;

// Add the numbers
$sum = $a + $b;

// Subtract the second number from the first one
$difference = $a - $b;

// Multiply the two numbers together
$product = $a * $b;

// Divide the second number by the first number and store the result in a variable
$division_result = $a / $b;

// Check if the division is even or odd using modulo operation
$result = $a % $b == 0 ? "The result of division is even" : "The result of division is not even";

echo "$sum = $difference = $product = $division_result = $result";
